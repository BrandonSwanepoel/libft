int	ft_atoi(const char *str)
{
	int		i;
	int		p;
	char	neg;

	neg = 1;
	i = 0;
	p = 0;
	while ((str[i] >= 9 && str[i] <= 13) || str[i] == 32)
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i + 1] >= '0' && str[i + 1] <= '9')
		{
			neg = (str[i] == '-') ? -1 : 1;
			i++;
		}
		else
			return (0);
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		p = p * 10 + str[i] - '0';
		i++;
	}
	return (p * neg);
}
