int	ft_strequ(char const *s1, char const *s2)
{
	int i;
	int s;

	i = 0;
	s = 0;
	if (s1 && s2)
	{
		while (s1[i] && s2[s])
		{
			s++;
			i++;
			if (i < s || i > s)
			{
				return (0);
				break ;
			}
			if (s1[i] != s2[s])
				return (0);
		}
	}
	return (1);
}
